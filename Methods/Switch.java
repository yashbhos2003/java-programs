class Switch{
    public static void main(String[] args){
      int num=0;

      switch(num){
        case 1:
           System.out.println("in case1");
           break;

        case 2:
           System.out.println("in case2");
           break;
  
        case 3:
           System.out.println("in case3");
           break;

        default:
           System.out.println("in default case");
      }
    }
}

